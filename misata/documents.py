"""
Document generation for Misata.

Render any generated DataFrame as real documents — HTML, PDF, Markdown,
or plain text — using Jinja2 templates.  Works with both built-in templates
(``"invoice"``, ``"patient_report"``, etc.) and custom template strings or files.

Quickstart::

    import misata

    tables = misata.generate("An ecommerce store with 200 orders")

    # Built-in template — no template file needed
    paths = misata.generate_documents(
        tables, template="invoice",
        table="orders", output_dir="/tmp/invoices"
    )
    print(f"Generated {len(paths)} invoices")

    # Custom Jinja2 template string
    html = \"\"\"
    <html><body>
      <h1>Order #{{ order_id }}</h1>
      <p>Customer: {{ customer_id }} — Amount: ${{ amount }}</p>
    </body></html>
    \"\"\"
    paths = misata.generate_documents(
        tables, template=html,
        table="orders", output_dir="/tmp/custom"
    )

    # Auto-detect a sensible template from column names
    paths = misata.generate_documents(
        tables, template="auto",
        table="orders", output_dir="/tmp/auto"
    )

PDF output requires the optional ``weasyprint`` package::

    pip install misata[documents]
"""

