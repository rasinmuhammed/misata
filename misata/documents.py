# ── Auto-detect template from column names ────────────────────────────────────

_TEMPLATE_SIGNALS: List[tuple] = [
    # (template_name, columns that strongly suggest this template)
    ("invoice",             {"order_id", "amount", "placed_at", "invoice_id", "total"}),
    ("transaction_receipt", {"transaction_id", "transaction_date", "debit", "credit",
                             "account_id", "balance", "fraud_flag"}),
    ("patient_report",      {"patient_id", "diagnosis", "condition", "medication",
                             "visit_date", "disease", "treatment"}),
    ("user_profile",        {"user_id", "email", "plan", "status", "signup_date",
                             "subscription_id", "churned"}),
]


def _detect_template(columns: List[str]) -> str:
    col_set = {c.lower() for c in columns}
    best_name, best_score = "generic", 0
    for tname, signals in _TEMPLATE_SIGNALS:
        score = len(col_set & signals)
        if score > best_score:
            best_name, best_score = tname, score
    return best_name


# ---------------------------------------------------------------------------
# DocumentTemplate
# ---------------------------------------------------------------------------

class DocumentTemplate:
    """Jinja2-backed document template for generating per-row documents.

    Args:
        source: One of:
                - A built-in template name (``"invoice"``, ``"patient_report"``,
                  ``"user_profile"``, ``"transaction_receipt"``, ``"generic"``,
                  ``"auto"``)
                - A Jinja2 template string (HTML, Markdown, or plain text)
                - A path to a ``.html``, ``.md``, or ``.txt`` template file
        format: Output format (``"html"``, ``"markdown"``, ``"txt"``).
                Used only when ``source`` is a built-in name.

    Example::

        tmpl = misata.DocumentTemplate("invoice")
        html = tmpl.render({"order_id": "1234", "amount": 99.95})
    """

    def __init__(
        self,
        source: str,
        format: Literal["html", "markdown", "txt"] = "html",
    ):
        self.format = format
        self._source_label = source
        self._jinja_template = self._load(source, format)

    # ------------------------------------------------------------------
    def _load(self, source: str, fmt: str):
        try:
            import jinja2
        except ImportError as exc:
            raise ImportError(
                "Jinja2 is required for document generation. "
                "Install it with: pip install jinja2"
            ) from exc

        env = jinja2.Environment(autoescape=False, undefined=jinja2.Undefined)

        # Built-in name
        if source in _BUILTIN_TEMPLATES or source == "auto":
            if source == "auto":
                # Deferred — resolved at render time once columns are known
                return None
            if fmt == "markdown":
                tmpl_str = _BUILTIN_MARKDOWN.get(source, _BUILTIN_MARKDOWN["generic"])
            else:
                tmpl_str = _BUILTIN_TEMPLATES[source]
            return env.from_string(tmpl_str)

        # File path
        path = Path(source).expanduser()
        if path.exists():
            tmpl_str = path.read_text(encoding="utf-8")
            return env.from_string(tmpl_str)

        # Raw template string (contains template syntax or multi-line)
        return env.from_string(source)

    def _resolve_auto(self, columns: List[str], fmt: str):
        """Resolve the 'auto' template once column names are known."""
        import jinja2
        env = jinja2.Environment(autoescape=False, undefined=jinja2.Undefined)
        name = _detect_template(columns)
        if fmt == "markdown":
            return env.from_string(_BUILTIN_MARKDOWN.get(name, _BUILTIN_MARKDOWN["generic"]))
        return env.from_string(_BUILTIN_TEMPLATES[name])

    def render(self, row: Dict[str, Any], *, title: str = "Record", subtitle: str = "") -> str:
        """Render a single row dict into document content.

        Args:
            row:      Dict of column → value (one DataFrame row).
            title:    Used as ``{{ _title }}`` in generic templates.
            subtitle: Used as ``{{ _subtitle }}`` in generic templates.

        Returns:
            Rendered string (HTML, Markdown, or plain text).
        """
        jt = self._jinja_template
        if jt is None:
            # auto mode — resolve from row keys
            jt = self._resolve_auto(list(row.keys()), self.format)
            self._jinja_template = jt   # cache for subsequent rows

        # Build _fields: exclude common sentinel/id-noise columns from the grid
        _SKIP = {"_fields", "_title", "_subtitle"}
        fields = {k: v for k, v in row.items() if k not in _SKIP and v is not None}

        ctx: Dict[str, Any] = {
            "_fields": fields,
            "_title": title,
            "_subtitle": subtitle,
        }
        ctx.update({k: v for k, v in row.items()})  # expose all cols directly
        return jt.render(**ctx)


# ---------------------------------------------------------------------------
# generate_documents()
# ---------------------------------------------------------------------------

def generate_documents(
    tables: Dict[str, Any],
    template: Union[str, "DocumentTemplate"],
    *,
    table: Optional[str] = None,
    output_dir: str,
    format: Literal["html", "pdf", "markdown", "txt"] = "html",
    prefix: str = "doc",
    filename_col: Optional[str] = None,
    limit: Optional[int] = None,
) -> List[str]:
    """Generate one document per row from a Misata table.

    Args:
        tables:       ``{table_name: pd.DataFrame}`` dict from
                      :func:`~misata.generate` or :func:`~misata.generate_from_schema`.
        template:     Template to use.  Accepts:

                      - Built-in names: ``"invoice"``, ``"patient_report"``,
                        ``"user_profile"``, ``"transaction_receipt"``,
                        ``"generic"``, ``"auto"``
                      - A Jinja2 template string
                      - A :class:`DocumentTemplate` instance
                      - A path to a ``.html`` / ``.md`` / ``.txt`` file
        table:        Which table to use.  Defaults to the first table
                      in ``tables``.
        output_dir:   Directory to write documents into (created if absent).
        format:       Output format — ``"html"`` (default), ``"pdf"``,
                      ``"markdown"``, or ``"txt"``.  PDF requires
                      ``pip install weasyprint``.
        prefix:       Filename prefix (default ``"doc"``).
        filename_col: Column to use as the filename stem instead of a
                      numeric counter (e.g. ``"order_id"``).
        limit:        Cap on number of documents to generate.

    Returns:
        List of paths to generated files.

    Raises:
        ImportError: If ``format="pdf"`` and weasyprint is not installed.
        ValueError:  If the specified table is not found.

    Example::

        tables = misata.generate("A fintech company with 100 transactions")
        paths = misata.generate_documents(
            tables, "transaction_receipt",
            output_dir="/tmp/receipts",
            format="html",
        )
        print(paths[0])  # /tmp/receipts/doc_1.html
    """
    import pandas as pd

    # Resolve DataFrame
    if table is None:
        table = next(iter(tables))
    if table not in tables:
        raise ValueError(
            f"Table '{table}' not found. Available: {list(tables.keys())}"
        )
    df = tables[table]
    if limit is not None:
        df = df.head(limit)

    # Resolve template
    fmt_for_template = "markdown" if format == "markdown" else (
        "txt" if format == "txt" else "html"
    )
    if isinstance(template, DocumentTemplate):
        doc_tmpl = template
    else:
        doc_tmpl = DocumentTemplate(str(template), format=fmt_for_template)

    # Extension
    ext_map = {"html": ".html", "pdf": ".html", "markdown": ".md", "txt": ".txt"}
    ext = ext_map.get(format, ".html")

    out_path = Path(output_dir).expanduser()
    out_path.mkdir(parents=True, exist_ok=True)

    paths: List[str] = []
    for idx, (_, row) in enumerate(df.iterrows(), start=1):
        row_dict = {k: _safe_val(v) for k, v in row.items()}

        if filename_col and filename_col in row_dict:
            stem = str(row_dict[filename_col]).replace("/", "_").replace(" ", "_")
        else:
            stem = f"{prefix}_{idx}"

        content = doc_tmpl.render(row_dict, title=table.replace("_", " ").title())
        html_file = out_path / f"{stem}{ext}"
        html_file.write_text(content, encoding="utf-8")

        if format == "pdf":
            pdf_file = out_path / f"{stem}.pdf"
            _html_to_pdf(str(html_file), str(pdf_file))
            html_file.unlink(missing_ok=True)
            paths.append(str(pdf_file))
        else:
            paths.append(str(html_file))

    return paths


def _safe_val(v: Any) -> Any:
    """Return a JSON-safe scalar; convert NaN/NaT to None."""
    import math
    try:
        import pandas as pd
        if pd.isna(v):
            return None
    except Exception:
        pass
    if isinstance(v, float) and math.isnan(v):
        return None
    return v


def _html_to_pdf(html_path: str, pdf_path: str) -> None:
    try:
        from weasyprint import HTML  # type: ignore
    except ImportError as exc:
        raise ImportError(
            "PDF output requires weasyprint. "
            "Install with: pip install 'misata[documents]'"
        ) from exc
    HTML(filename=html_path).write_pdf(pdf_path)


