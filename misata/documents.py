def list_document_templates() -> List[str]:
    """Return names of all built-in document templates.

    Example::

        misata.list_document_templates()
        # ['invoice', 'patient_report', 'transaction_receipt', 'user_profile', 'generic']
    """
    return sorted(_BUILTIN_TEMPLATES.keys())
